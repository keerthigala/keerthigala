class Comparision
{
    public static void main(String args[])
    {
        int a=5;
        int b=3;
        {
            if(a>b)
                System.out.print(a+ "is greater than" +b);
            else if(a<b)
                System.out.print(b+ "is greater than" +a);
            else
                System.out.print(a+ "is equal to" +b);
        }
    }
}