public class BreakStatement
{
    public static void main(String args[]) {
        String day = "Thr";
        switch (day) {
            case "Mon":
                System.out.print("Monday");
                break;
            case "Tue":
                System.out.print("Tuesday");
                break;
                case "Wed":
                System.out.print("Wednesday");
                break;
            case "Thr":
                System.out.print("Thrusday");
                break;
                default:
                System.out.print("No day");
        }
    }
}
 